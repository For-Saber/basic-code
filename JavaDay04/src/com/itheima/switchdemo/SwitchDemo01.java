package com.itheima.switchdemo;

public class SwitchDemo01 {
    public static void main(String[] args) {
    int num=1;
    switch (num){
        case 1 ->{
            System.out.println("一");
        }
        case 2 ->{
            System.out.println("二");
        }
        default->{
            System.out.println("无");
        }
    }
    }
}
